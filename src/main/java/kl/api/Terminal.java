package kl.api;

public class Terminal {

	private String terminalType;
	private String terminalSN;
	private String terminalIMEI;
	private String terminalManufacture;
	private String terminalBrand;
	private String termianlModel;
	public String getTerminalType() {
		return terminalType;
	}
	public void setTerminalType(String terminalType) {
		this.terminalType = terminalType;
	}
	public String getTerminalSN() {
		return terminalSN;
	}
	public void setTerminalSN(String terminalSN) {
		this.terminalSN = terminalSN;
	}
	public String getTerminalIMEI() {
		return terminalIMEI;
	}
	public void setTerminalIMEI(String terminalIMEI) {
		this.terminalIMEI = terminalIMEI;
	}
	public String getTerminalManufacture() {
		return terminalManufacture;
	}
	public void setTerminalManufacture(String terminalManufacture) {
		this.terminalManufacture = terminalManufacture;
	}
	public String getTerminalBrand() {
		return terminalBrand;
	}
	public void setTerminalBrand(String terminalBrand) {
		this.terminalBrand = terminalBrand;
	}
	public String getTermianlModel() {
		return termianlModel;
	}
	public void setTermianlModel(String termianlModel) {
		this.termianlModel = termianlModel;
	}
	
	
	
}
